def build_paper_profile(paper):
    terms = ", ".join(word for word, _ in keyword_list(paper["text"], 8))
    doi_line = paper.get("doi") or "Not detected"
    return "\n".join(
        [
            f"- Title: {paper['title']}",
            f"- File: {paper['file_name']}",
            f"- Author metadata: {paper['stats'].get('author') or 'Not provided'}",
            f"- Pages with text: {len(paper['pages'])}",
            f"- Words extracted: {paper['word_count']:,}",
            f"- DOI: {doi_line}",
            f"- Top terms: {terms or 'Not enough text extracted'}",
            f"- Working abstract: {paper['abstract'] or 'No readable text extracted.'}",
            "",
            "## Citation",
            paper.get("citation") or basic_citation(paper),
        ]
    )


def build_library_profile(papers):
    if len(papers) == 1:
        return build_paper_profile(papers[0])

    all_terms = Counter()
    for paper in papers:
        all_terms.update(dict(keyword_list(paper["text"], 12)))
    titles = "\n".join(
        f"- Paper {paper['paper_index']}: {paper['title']} ({paper['word_count']:,} words)"
        for paper in papers
    )
    return "\n".join(
        [
            f"- Papers in active context: {len(papers)}",
            f"- Total pages with readable text: {sum(len(paper['pages']) for paper in papers)}",
            f"- Total words extracted: {sum(paper['word_count'] for paper in papers):,}",
            f"- Shared top terms: {', '.join(term for term, _ in all_terms.most_common(12))}",
            "",
            "## Active Papers",
            titles,
        ]
    )


def build_combined_corpus(papers):
    sections = []
    for paper in papers:
        sections.append(
            "\n".join(
                [
                    f"===== PAPER {paper['paper_index']}: {paper['title']} =====",
                    f"File name: {paper['file_name']}",
                    f"DOI: {paper.get('doi') or 'Not detected'}",
                    f"Citation: {paper.get('citation') or basic_citation(paper)}",
                    "",
                    paper["text"],
                ]
            )
        )
    return "\n\n".join(sections)


def prepare_uploaded_papers(uploaded_files, model_name, use_ai_citations):
    papers = []
    errors = []
    next_index = len(st.session_state.get("papers", [])) + 1
    existing_digests = {paper["digest"] for paper in st.session_state.get("papers", [])}

    for uploaded_file in uploaded_files:
        file_bytes = uploaded_file.getvalue()
        digest = file_digest(file_bytes)
        if digest in existing_digests:
            continue

        try:
            stats = get_pdf_stats(uploaded_file)
            pages = extract_pages_from_pdf(uploaded_file)
        except Exception as exc:
            errors.append(f"{uploaded_file.name}: {exc}")
            continue

        text = "\n".join(page["text"] for page in pages)
        if not text.strip():
            errors.append(f"{uploaded_file.name}: no readable text found")
            continue

        paper = {
            "digest": digest,
            "paper_index": next_index,
            "file_name": uploaded_file.name,
            "stats": stats,
            "pages": pages,
            "text": text,
            "title": stats.get("title") or guess_title(text, uploaded_file.name),
            "doi": extract_doi(text),
            "abstract": first_words(text, 500),
            "word_count": len(text.split()),
            "char_count": len(text),
        }
        paper["citation"] = generate_ai_citation(paper, model_name) if use_ai_citations else basic_citation(paper)
        papers.append(paper)
        existing_digests.add(digest)
        next_index += 1

    return papers, errors


def format_inline_markdown(text):
    text = html.escape(text)
    text = re.sub(r"`([^`]+)`", r"<code>\1</code>", text)
    text = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", text)
    text = re.sub(r"\*([^*]+)\*", r"<em>\1</em>", text)
    return text


def markdown_to_html(text):
    html_parts = []
    in_list = False
    in_code = False
    code_lines = []

    def close_list():
        nonlocal in_list
        if in_list:
            html_parts.append("</ul>")
            in_list = False

    for raw_line in (text or "").splitlines():
        line = raw_line.rstrip()
        if line.strip().startswith("```"):
            if in_code:
                html_parts.append(f"<pre><code>{html.escape(chr(10).join(code_lines))}</code></pre>")
                code_lines = []
                in_code = False
            else:
                close_list()
                in_code = True
            continue
        if in_code:
            code_lines.append(line)
            continue

        stripped = line.strip()
        if not stripped:
            close_list()
            continue
        if stripped.startswith("---"):
            close_list()
            html_parts.append("<hr>")
            continue
        if stripped.startswith("### "):
            close_list()
            html_parts.append(f"<h4>{format_inline_markdown(stripped[4:])}</h4>")
            continue
        if stripped.startswith("## "):
            close_list()
            html_parts.append(f"<h3>{format_inline_markdown(stripped[3:])}</h3>")
            continue
        if stripped.startswith("# "):
            close_list()
            html_parts.append(f"<h2>{format_inline_markdown(stripped[2:])}</h2>")
            continue
        if stripped.startswith(("- ", "* ")):
            if not in_list:
                html_parts.append("<ul>")
                in_list = True
            html_parts.append(f"<li>{format_inline_markdown(stripped[2:])}</li>")
            continue

        close_list()
        html_parts.append(f"<p>{format_inline_markdown(stripped)}</p>")

    close_list()
    if in_code:
        html_parts.append(f"<pre><code>{html.escape(chr(10).join(code_lines))}</code></pre>")
    return "\n".join(html_parts)


