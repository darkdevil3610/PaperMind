def run_analysis(paper_text, model_name, char_limit):
    steps = [
        ("Summary", lambda: run_summary_agent(paper_text, model_name, char_limit)),
        ("Methodology", lambda: run_methodology_agent(paper_text, model_name, char_limit)),
        ("Evaluation", lambda: run_evaluation_agent(paper_text, model_name, char_limit)),
        ("Claims", lambda: run_claims_agent(paper_text, model_name, char_limit)),
        ("Research Mentor", lambda: run_research_mentor_agent(paper_text, model_name, char_limit)),
    ]

    outputs = {}
    progress = st.progress(0)
    status = st.status("Running multi-agent analysis", expanded=True)
    for index, (name, task) in enumerate(steps, start=1):
        with st.spinner(f"{name} agent is working..."):
            outputs[name] = task()
        progress.progress(int(index / (len(steps) + 1) * 100))
        status.write(f"{name} agent completed")

    with st.spinner("Safety review agent is checking the generated analysis..."):
        outputs["Safety"] = run_safety_agent(
            outputs["Summary"],
            outputs["Methodology"],
            outputs["Evaluation"],
            outputs["Research Mentor"],
            outputs["Claims"],
            model_name,
        )
    progress.progress(100)
    status.update(label="Analysis complete", state="complete", expanded=False)
    return outputs


def answer_with_rag(question, hits, model_name, chat_history):
    context_blocks = []
    for hit in hits:
        metadata = hit["metadata"]
        context_blocks.append(
            f"[{source_label(hit)} | {metadata.get('title')} | DOI: {metadata.get('doi') or 'not detected'}]\n{hit['text']}"
        )
    history = "\n".join(f"{item['role']}: {item['content']}" for item in chat_history[-6:])
    prompt = f"""
You are PaperMind, a critical research assistant.

Answer the user using only the retrieved PDF context where possible.
Use source labels like [Paper 1, p. 4] after claims that come from a source.
If the retrieved context is insufficient, say what is missing.
Be concise, analytical, and avoid overclaiming.

Recent chat:
{history}

Retrieved context:
{chr(10).join(context_blocks)}

Question:
{question}
"""
    return run_gemini(prompt, model_name)


