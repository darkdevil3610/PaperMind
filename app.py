def clean_markdown(text):
    text = html.escape(text)
    text = re.sub(r"\*\*(.*?)\*\*", r"<b>\1</b>", text)
    return text


def create_pdf_report(report_text):
    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=A4,
        rightMargin=48,
        leftMargin=48,
        topMargin=46,
        bottomMargin=46,
    )
    styles = getSampleStyleSheet()
    title_style = ParagraphStyle(
        "PaperMindTitle",
        parent=styles["Title"],
        fontSize=22,
        leading=28,
        spaceAfter=18,
        textColor=colors.HexColor("#1f3a5f"),
    )
    heading_style = ParagraphStyle(
        "PaperMindHeading",
        parent=styles["Heading2"],
        fontSize=14,
        leading=18,
        spaceBefore=14,
        spaceAfter=7,
        textColor=colors.HexColor("#1f3a5f"),
    )
    body_style = ParagraphStyle(
        "PaperMindBody",
        parent=styles["BodyText"],
        fontSize=9.5,
        leading=13,
        spaceAfter=5,
    )
    bullet_style = ParagraphStyle(
        "PaperMindBullet",
        parent=body_style,
        leftIndent=17,
        bulletIndent=7,
    )

    story = [
        Paragraph(APP_NAME, title_style),
        Paragraph("RAG-Powered Research Report", heading_style),
        Paragraph(
            "Generated by an AI research assistant. Review all outputs before academic, clinical, legal, or professional use.",
            body_style,
        ),
        PageBreak(),
    ]

    for line in report_text.splitlines():
        line = line.strip()
        if not line:
            story.append(Spacer(1, 6))
        elif line.startswith("# "):
            story.append(Paragraph(clean_markdown(line[2:]), title_style))
        elif line.startswith("## "):
            story.append(Paragraph(clean_markdown(line[3:]), heading_style))
        elif line.startswith("### "):
            story.append(Paragraph(clean_markdown(line[4:]), heading_style))
        elif line.startswith(("- ", "* ")):
            story.append(Paragraph(clean_markdown(line[2:]), bullet_style, bulletText="•"))
        elif line.startswith("---"):
            story.append(Spacer(1, 10))
        else:
            story.append(Paragraph(clean_markdown(line), body_style))

    doc.build(story)
    buffer.seek(0)
    return buffer


def guess_title(text, fallback="Untitled paper"):
    for line in text.splitlines()[:25]:
        line = re.sub(r"\s+", " ", line).strip()
        if 18 <= len(line) <= 180 and not line.lower().startswith(("abstract", "keywords")):
            return line
    return fallback


def keyword_list(text, limit=12):
    words = re.findall(r"[A-Za-z][A-Za-z\-]{3,}", text.lower())
    counts = Counter(word for word in words if word not in STOPWORDS)
    return counts.most_common(limit)


def basic_citation(paper):
    doi = paper.get("doi")
    suffix = f" https://doi.org/{doi}" if doi else ""
    return f"{paper['title']}. Source file: {paper['file_name']}.{suffix}"


